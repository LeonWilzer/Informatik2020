package nim;

public class Pot {
	private int sticks;
	private int chips;
	public Pot() {
		sticks = 0;
		chips = 0;
	}
	public int getChips(){
		return chips;
	}
	public void setChips(int pChips){
		chips = pChips;
	}
	public int getSticks(){
		return chips;
	}
	public void setSticks(int pSticks){
		chips = pSticks;
	}
}
